.init_std_git <- function(git, commit) {
  if (!git) {
    return(invisible(FALSE))
  }
  .git_system_setup()
  .git_init()
 .ignore_auto()
  if (commit) {
    .init_git_commit()
  }
  .init_git_suggest_git()
  invisible(TRUE)
}
